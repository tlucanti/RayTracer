#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*lst;

	lst = (t_list *)malloc(sizeof(t_list));
	if (lst == NULLPTR)
		return (NULLPTR);
	lst->content = content;
	lst->next = NULLPTR;
	return (lst);
}
